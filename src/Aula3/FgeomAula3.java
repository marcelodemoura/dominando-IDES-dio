package Aula3;

public class FgeomAula3 {

    public static double quadrado(double area){

        return area * area;
    }

    public static double retangulo(double area1, double area2){

        return area1 * area2;
    }

    public static double trapezio(double base1, double base2, double altura){

        return  ((base1 + base2) * altura)/ 2;
    }
}
